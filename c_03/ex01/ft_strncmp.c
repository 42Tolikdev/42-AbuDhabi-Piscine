int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	diff;

	diff = 0;
	while ((n > 0) && (*s1 || *s2))
	{
		diff = *s1 - *s2;
		if (diff != 0)
			break ;
		s1++;
		s2++;
		n--;
	}
	return (diff);
}
