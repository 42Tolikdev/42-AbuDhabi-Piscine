int		ft_strcmp(char *s1, char *s2)
{
	int		diff;

	diff = 0;
	while (*s1 || *s2)
	{
		diff = *s1 - *s2;
		if (diff != 0)
			break ;
		s1++;
		s2++;
	}
	return (diff);
}
